function mostra_idade(nome, idade) {
    return `A idade de ${nome} é ${idade}`
}
function mostra_cidade(nome, cidade) {
    return `A cidade de ${nome} é ${cidade}`
}
function mostra_hobby(nome, hobby) {
    return `A hobby de ${nome} é ${hobby}`
}

export {
    mostra_idade,
    mostra_cidade,
    mostra_hobby,
}